#include <LaserBoy/SDL_GUI.hpp>

//############################################################################
int main(int argc, char *argv[])
{
    int xres = 640,
        yres = 480;

    if(argc >= 3)
    {
        xres = atoi(argv[1]);
        yres = atoi(argv[2]);
    }

    LaserBoy::SDL_GUI app(xres, yres);

    return app.start_menu_loop();
}

